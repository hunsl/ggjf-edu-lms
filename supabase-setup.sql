-- ==========================================
-- 7. 출결률 자동 계산 함수 & 트리거
-- 출결 INSERT/UPDATE/DELETE 시 student.rate 자동 갱신
-- ==========================================
CREATE OR REPLACE FUNCTION update_student_rate()
RETURNS TRIGGER AS $$
DECLARE
  v_student_id BIGINT;
  v_cid        BIGINT;
  v_date_from  TEXT;
  v_date_to    TEXT;
  total_days   INTEGER;
  attended_days INTEGER;
  new_rate     NUMERIC;
BEGIN
  -- 영향받는 student_id 결정
  IF TG_OP = 'DELETE' THEN
    v_student_id := OLD.student_id;
  ELSE
    v_student_id := NEW.student_id;
  END IF;

  -- 해당 학생의 과정 정보 가져오기
  SELECT cid INTO v_cid FROM students WHERE id = v_student_id;
  IF v_cid IS NULL THEN
    IF TG_OP = 'DELETE' THEN RETURN OLD; ELSE RETURN NEW; END IF;
  END IF;

  SELECT date_from, date_to INTO v_date_from, v_date_to
  FROM courses WHERE id = v_cid;

  -- 과정 전체 수업일 수 계산 (오늘까지만)
  SELECT COUNT(DISTINCT gs::date)
  INTO total_days
  FROM generate_series(
    v_date_from::date,
    LEAST(COALESCE(v_date_to::date, CURRENT_DATE), CURRENT_DATE),
    '1 day'::interval
  ) gs
  WHERE EXTRACT(DOW FROM gs::date) NOT IN (0, 6); -- 주말 제외

  IF total_days = 0 THEN RETURN NEW; END IF;

  -- 학생이 실제 출석한 날 수 (입실(type='in') 기준)
  SELECT COUNT(DISTINCT date)
  INTO attended_days
  FROM attendance
  WHERE student_id = v_student_id AND type = 'in';

  new_rate := ROUND(LEAST((attended_days::NUMERIC / total_days) * 100, 100), 1);

  UPDATE students SET rate = new_rate WHERE id = v_student_id;

  IF TG_OP = 'DELETE' THEN RETURN OLD; ELSE RETURN NEW; END IF;
END;
$$ LANGUAGE plpgsql;

-- 기존 트리거 제거 후 재생성
DROP TRIGGER IF EXISTS trg_attendance_rate ON attendance;
CREATE TRIGGER trg_attendance_rate
  AFTER INSERT OR UPDATE OR DELETE ON attendance
  FOR EACH ROW EXECUTE FUNCTION update_student_rate();

