-- 과정 테이블
CREATE TABLE IF NOT EXISTS courses (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  cat         TEXT,
  cc          TEXT,
  name        TEXT NOT NULL,
  code        TEXT,
  date_from   TEXT,
  date_to     TEXT,
  period      TEXT,
  method      TEXT,
  hours       NUMERIC,
  tgt         INTEGER DEFAULT 0,
  c_goal      INTEGER DEFAULT 0,
  e_goal      INTEGER DEFAULT 0,
  sched_days  TEXT,
  sched_time_from TEXT,
  sched_time_to   TEXT,
  notes       TEXT,
  pdf_name    TEXT,
  pdf_data    TEXT,
  links       JSONB DEFAULT '[]'::JSONB,
  created_at  TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 훈련생 테이블 (id는 IDENTITY — 앱이 직접 생성하지 않음)
CREATE TABLE IF NOT EXISTS students (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  cid         BIGINT REFERENCES courses(id) ON DELETE SET NULL,
  name        TEXT NOT NULL,
  gender      TEXT DEFAULT '',
  birth       TEXT,
  id_back     TEXT DEFAULT '',
  phone       TEXT DEFAULT '',
  phone2      TEXT DEFAULT '',
  addr_city   TEXT DEFAULT '',
  addr_detail TEXT DEFAULT '',
  edu         TEXT DEFAULT '',
  major       TEXT DEFAULT '',
  career      TEXT DEFAULT '',
  cert        TEXT DEFAULT '',
  status      TEXT DEFAULT '미취업',
  unemp       BOOLEAN DEFAULT FALSE,
  disabled    BOOLEAN DEFAULT FALSE,
  veteran     BOOLEAN DEFAULT FALSE,
  itv_date    TEXT,
  itv_score   NUMERIC,
  itv_grade   TEXT DEFAULT '',
  itv_pass    BOOLEAN DEFAULT FALSE,
  memo        TEXT DEFAULT '',
  rate        NUMERIC DEFAULT 0,
  created_at  TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 출결 테이블
CREATE TABLE IF NOT EXISTS attendance (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  student_id  BIGINT REFERENCES students(id) ON DELETE CASCADE,
  date        TEXT NOT NULL,
  type        TEXT NOT NULL CHECK (type IN ('in','out')),
  time        TEXT,
  status      TEXT DEFAULT 'O',
  created_at  TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 강사 테이블
CREATE TABLE IF NOT EXISTS instructors (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name        TEXT NOT NULL,
  type        TEXT DEFAULT '주강사',
  subject     TEXT DEFAULT '',
  phone       TEXT DEFAULT '',
  email       TEXT DEFAULT '',
  career      TEXT DEFAULT '',
  cert        TEXT DEFAULT '',
  cids        JSONB DEFAULT '[]'::JSONB,
  note        TEXT DEFAULT '',
  created_at  TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 강의실 테이블
CREATE TABLE IF NOT EXISTS rooms (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name        TEXT NOT NULL,
  addr        TEXT DEFAULT '',
  capacity    INTEGER DEFAULT 20,
  equip       TEXT DEFAULT '',
  created_at  TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 강의실 예약(배정) 테이블
CREATE TABLE IF NOT EXISTS room_bookings (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  room_id     BIGINT REFERENCES rooms(id) ON DELETE CASCADE,
  course_id   BIGINT REFERENCES courses(id) ON DELETE CASCADE,
  label       TEXT DEFAULT '',
  start_date  TEXT NOT NULL,
  end_date    TEXT NOT NULL,
  color       TEXT DEFAULT '#EA580C',
  created_at  TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 계정 테이블
CREATE TABLE IF NOT EXISTS accounts (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name        TEXT NOT NULL,
  role        TEXT DEFAULT 'staff',
  pw          TEXT NOT NULL,
  created_at  TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

